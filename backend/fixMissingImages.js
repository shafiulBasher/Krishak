const fixMissingImages = async () => {
  try {
    await mongoose.connect(process.env.MONGO_URI);
    const products = await Product.find().lean();
    
    console.log(`\nğŸ”§ Fixing missing images for ${products.length} products...\n`);
    
    const uploadsDir = path.join(__dirname, 'uploads', 'products');
    if (!fs.existsSync(uploadsDir)) {
      fs.mkdirSync(uploadsDir, { recursive: true });
    }
    
    let created = 0;
    
    products.forEach(product => {
      if (product.photos && product.photos.length > 0) {
        product.photos.forEach(photoPath => {
          const filename = path.basename(photoPath);
          const filepath = path.join(uploadsDir, filename);
          
          if (!fs.existsSync(filepath)) {
            const ext = path.extname(filename).toLowerCase();
            const data = ext === '.jpg' || ext === '.jpeg' ? createTestJPEG() : createTestPNG();
            fs.writeFileSync(filepath, data);
            created++;
            console.log(`âœ… Created: ${filename}`);
          }
        });
      }
    });
    
    console.log(`\nâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•`);
    console.log(`âœ… Created ${created} placeholder image files`);
    console.log(`â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•\n`);
    
    process.exit(0);
  } catch (err) {
    console.error('âŒ Error:', err.message);
    process.exit(1);
  }
};

